/*------------------------------------------------------------------------------
** TM_StartTimer()
**------------------------------------------------------------------------------
*/

void TM_StartTimer()
{

   /*
   ** Timer 1 start
   */

	TCCR1A = 0x00;
	TCCR1B = 0x0C;
	TCCR1C = 0x00;
	TCNT1 = 0x0000;
	OCR1A= 62499;//waiting

	TIMSK1 = 0x00;
	TIFR1= 0x2F;
	

   

   TM_iResponseTime = 0;

}/* end TM_StartTimer */




BOOL TM_TimeOut()
{

   /*
   ** Wait for response
   */

   if( TIFR1 & 0x02 )
   {

      /*
      ** Timer 1 stop
      */

      TIFR1= 0x2F;
      return( TRUE );

   }
   else
   {

      return( FALSE );

   }/* end if timer timed out */

}/* end TM_TimeOut */


