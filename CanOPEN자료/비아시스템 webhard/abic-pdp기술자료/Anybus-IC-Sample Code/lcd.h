#ifndef LCD_H
#define LCD_H


/*******************************************************************************
**
** Public Services Definitions
**
********************************************************************************
*/

/*------------------------------------------------------------------------------
**
** LCD_ClearDisplay()
**
** Clear the display of all its contents.
**
**------------------------------------------------------------------------------
**
** Inputs:
**    None
**
** Outputs:
**    None
**
** Usage:
**    LCD_ClearDisplay()
**
**------------------------------------------------------------------------------
*/

void LCD_ClearDisplay( void );


/*------------------------------------------------------------------------------
**
** LCD_Init()
**
** Initiates the LCD
**
**------------------------------------------------------------------------------
**
** Inputs:
**    None
**
** Outputs:
**    None
**
** Usage:
**    LCD_Init();
**
**------------------------------------------------------------------------------
*/

void LCD_Init( void );


/*------------------------------------------------------------------------------
**
** LCD_PrintString()
**
** Print a text string on the selected row.
**
**------------------------------------------------------------------------------
**
** Inputs:
**    abString     - pointer to the string to print
**
** Outputs:
**    None
**
** Usage:
**    LCD_PrintString( "This is my string!" );
**
**------------------------------------------------------------------------------
*/

void LCD_PrintString( UINT8 *abString );


/*------------------------------------------------------------------------------
**
** LCD_PrintHex()
**
** Prints a byte value in hex on the LCD
**
**------------------------------------------------------------------------------
**
** Inputs:
**    bValue      - Value to print
**
** Outputs:
**    None
**
** Usage:
**    LCD_PrintHex( 0x45 );
**
**------------------------------------------------------------------------------
*/

void LCD_PrintHex( UINT8 bValue );


/*------------------------------------------------------------------------------
**
** LCD_SelectRow()
**
** Selects wish of the to rows on the LCD that should be used.
**
**------------------------------------------------------------------------------
**
** Inputs:
**    bRow        - Row number to select ( 0 or 1 )
**
** Outputs:
**    None
**
** Usage:
**    LCD_SelectRow( 1 );
**
**------------------------------------------------------------------------------
*/

void LCD_SelectRow( UINT8 bRow );


#endif  /* inclusion lock */

